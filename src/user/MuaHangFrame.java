    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int i=1;
        if (jTextFieldSoLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhập số lượng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else if (jDateChooserNgayMua.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Chọn ngày mua !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } 
         else if (jTextFieldTenKhachHang.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhập tên khách hàng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } 
          else if (jTextFieldSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhập số điện thoại !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } 
        else {

            if (!(soluong <= 0)) {
                int soluongnhap = Integer.parseInt(jTextFieldSoLuong.getText());
                if (soluongnhap != 0) {
                    if(!(soluongnhap>soluong)){
                
                 
                    String tensanpham=jTextFieldTen.getText();
                    String tong=String.format(Locale.US,"%.2f", gia*(double)soluongnhap);
                    Object[] row={stt,tensanpham,gia,soluongnhap,tong};
                    model=(DefaultTableModel) jTableDonHang.getModel();
                    model.addRow(row);
                    tinhtien+=gia*(double)soluongnhap;
                    jTextFieldThanhTien.setText(String.format(Locale.US,"%.2f",tinhtien));
                    
                    jTextFieldTen.setText("");
                    jTextFieldSoLuong.setText("");
                    clear();
                    stt++;
                }
                    else{
                        JOptionPane.showMessageDialog(this, "Không đủ sản phẩm !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Nhập số lượng lớn hơn 0 !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            } 
            else if(jDateChooserNgayMua.getDate()==null) {
                JOptionPane.showMessageDialog(this, "Chọn ngày mua !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(this, "Sản phẩm đã hết !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        model = (DefaultTableModel) jTableDonHang.getModel();
        String email = GiaoDienNguoiDungFrame.jLabelEmail.getText().toString();
        if (jDateChooserNgayMua.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Chọn ngày mua !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        if (jTextFieldSDT == null) {
            JOptionPane.showMessageDialog(this, "Chọn số điện thoại !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        if (model.getRowCount() > 0) {

            int id_donhang = Integer.parseInt(jTextFieldIDDonHang.getText());
            String tenKH = jTextFieldTenKhachHang.getText();
            String sdt = jTextFieldSDT.getText();
            double gia = Double.valueOf(jTextFieldThanhTien.getText());
            java.util.Date utilNgayMua = jDateChooserNgayMua.getDate();
            java.sql.Date ngaymua = new java.sql.Date(utilNgayMua.getTime());
            int id_nv=nvDAO.getId(email);
            int id_kh=khDAO.getId(sdt);
            HoaDon hd = new HoaDon(id_donhang, tenKH, sdt, gia, ngaymua, id_nv, id_kh);
            dhDAO.insert(hd, email);
            for (int i = 0; i < model.getRowCount(); i++) {

                int stt = Integer.parseInt(model.getValueAt(i, 0).toString());
                String tensp = model.getValueAt(i, 1).toString();
                double giatien = Double.parseDouble(model.getValueAt(i, 2).toString());
                int sl = Integer.parseInt(model.getValueAt(i, 3).toString());
                double tong = Double.parseDouble(model.getValueAt(i, 4).toString());
                int id_sp = spDAO.getID(tensp);

                CTHD ct = new CTHD(id_donhang, stt, tensp, id_sp, sl, tong);
                ctDAO.insert(ct);
                int SLmoi = dhDAO.getSLSP(tensp) - sl;
                dhDAO.updateSanPham(tensp, SLmoi);
            }
            if(flagDiem == 1)
            {
                khDAO.updateDiem(id_kh, 0);
            }
            double tichdiem = Double.valueOf(jTextFieldThanhTien.getText()) / 1000.0;
            double diemmoi = khDAO.getDiem(id_kh) + tichdiem;
            khDAO.updateDiem(id_kh, diemmoi);

            //java.util.Date utilNgayMua = jDateChooserNgayMua.getDate();
            java.util.Calendar calendar = java.util.Calendar.getInstance();
            calendar.setTime(utilNgayMua);
            int month = calendar.get(java.util.Calendar.MONTH)+1;
            
            double doanhthuhientai = Double.valueOf(jTextFieldThanhTien.getText());
            double doanhthumoi=dtDAO.getDoanhThu(id_nv, month)+doanhthuhientai;
            dtDAO.updateDoanhThu(id_nv, month, doanhthumoi);
            
            

            JOptionPane.showMessageDialog(this, "Mua hàng thành công !!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            init();
            refresh();
        } else {

            JOptionPane.showMessageDialog(this, "Giỏ hàng trống !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

