    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int select=jTableDonHang.getSelectedRow();
        if(select==-1){
        
            JOptionPane.showMessageDialog(this, "Chọn sản phẩm !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        else{
        
            double Gia = Double.parseDouble(jTableDonHang.getValueAt(select, 4).toString());
            tong = Gia;
            tinhtien-=tong;
            jTextFieldThanhTien.setText(String.format(Locale.US,"%.2f",tinhtien));
             model=(DefaultTableModel) jTableDonHang.getModel();
             model.removeRow(select);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

