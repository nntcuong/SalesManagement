    private void jButtonTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTimKiemActionPerformed
        if(this.jTextFieldTuKhoa.getText().equals("")) JOptionPane.showMessageDialog(this, "Mục tìm kiếm rỗng!!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        if(this.jComboBoxTK.getSelectedItem().toString().equals("ID") && !this.jTextFieldTuKhoa.getText().equals("")) this.nvDAO.searchForID(Integer.parseInt(this.jTextFieldTuKhoa.getText()),this.jTableNhanVien);
        if(this.jComboBoxTK.getSelectedItem().toString().equals("SĐT")) this.nvDAO.searchForSDT(this.jTextFieldTuKhoa.getText(),this.jTableNhanVien);
        if(this.jComboBoxTK.getSelectedItem().toString().equals("Họ Tên")) this.nvDAO.searchForName(this.jTextFieldTuKhoa.getText(),this.jTableNhanVien);
    }//GEN-LAST:event_jButtonTimKiemActionPerformed

