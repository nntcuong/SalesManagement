    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (kiemTraKhongRong()) {

            int id = Integer.parseInt(jTextFieldID.getText());
            String ten = jTextFieldTen.getText();
            String diaChi = jTextFieldDiaChi.getText();
            String sdt = jTextFieldSDT.getText();
            String email=jTextFieldEmail.getText();
  
          

            NhaCungCap ncc=new NhaCungCap(id, ten, diaChi, sdt, email);

            if (!nccDAO.kiemTraIDKhongDuocThayDoi((NhaCungCap) ncc)) {
                nccDAO.update((NhaCungCap) ncc);
                QuanLyNhanVienFrame ql = new QuanLyNhanVienFrame();
                ql.setVisible(true);
                ql.pack();

                this.dispose();
                JOptionPane.showMessageDialog(this, "Cập nhật thành công ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Không được thay đổi ID ", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

